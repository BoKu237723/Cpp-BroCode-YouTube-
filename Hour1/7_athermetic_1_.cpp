#include <iostream>
int main(){
    int students = 20;
    students += 1;
    std::cout << students << "\n";

    int players = 20;
    players = players + 1;
    std::cout << players << "\n";

    int users = 20;
    users ++;
    std::cout << users << "\n";


    int programmers = 20;
    programmers -= 1;
    std::cout << programmers << "\n";

    int doctors = 20;
    doctors = doctors - 1;
    std::cout << doctors << "\n";

    int engineers = 20;
    engineers--;
    std::cout << engineers << "\n";

    int gamers;
    gamers = 20;
    gamers = gamers * 2;
    std::cout << gamers << "\n";

    int child;
    child = 20;
    child *= 2;
    std::cout << child << "\n";
    
    int therapy;
    therapy = 20;
    therapy = therapy / 2;
    std::cout << therapy << "\n";

    int sisters;
    sisters = 20;
    sisters /= 2;
    std::cout << sisters << "\n";

    int price;
    price = 20;
    price % 3;
    std::cout << price << "\n";

    int number;
    number = 20;
    number %= 3;
    std::cout << number << "\n";


    return 0;
}

